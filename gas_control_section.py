class Gas_control_section:
    def __init__(self, capacity, pressure=3):
        self.capacity=capacity
        self.pressure=pressure
    def max_flow(self):
        return int(((self.capacity*3600)//(34*100)+1)*100)
    def max_pressure(self):
        if self.pressure<4.5:
            return 6
    def price_str(self):
        return str(self.max_flow()) + " x " + str(self.pressure)
    def size(self):
        return SIZES[(self.max_flow(),self.pressure)]

